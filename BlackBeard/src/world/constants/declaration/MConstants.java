package world.constants.declaration;

/**
 * Michael Ferolito had a computer. It was old, and had java 1.6. Processing
 * threw some BS about needing a newer version, so Michael wrote his own version
 * of PConstants.
 * 
 * @author Michael Ferolito
 * @version 2
 * @since 2
 */
public interface MConstants {
	/**
	 * 3.1415926515897932382646355*2
	 */
	public static final double TWO_PI = 2 * Math.PI;
	/**
	 * Math.pi
	 */
	public static final double PI = Math.PI;
	/**
	 * Math.pi/2
	 */
	public static final double HALF_PI = Math.PI / 2;
	/**
	 * Math.pi*2
	 */
	public static final double TAU = TWO_PI;

}
